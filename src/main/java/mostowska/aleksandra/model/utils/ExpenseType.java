package mostowska.aleksandra.model.utils;

/**
 * Represents different types of expenses that a user can record.
 * Includes predefined options as well as a `CUSTOM` option for user-defined expenses.
 */
public enum ExpenseType {
    GROCERIES,
    RENT,              // Expenses for rent
    UTILITIES,
    TRANSPORT,
    ENTERTAINMENT,
    HEALTH,
    INSURANCE,
    EDUCATION,
    CLOTHING,
    PERSONAL_CARE,
    HOUSEHOLD,
    SUBSCRIPTIONS,
    LOANS,
    CHARITY,
    PETS,
    VACATION,
    HOBBIES,
    GIFTS,
    CUSTOM
}
