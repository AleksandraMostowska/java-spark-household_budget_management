package mostowska.aleksandra.model.utils;

/**
 * Represents various types of savings goals that a user can set.
 * This enum provides predefined goal types as well as a `CUSTOM` option
 * for user-defined savings goals.
 */
public enum SavingsGoalType {
    VACATION,
    CAR,
    RETIREMENT,
    HOUSE,
    EDUCATION,
    CUSTOM
}
