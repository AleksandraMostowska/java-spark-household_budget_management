package mostowska.aleksandra.model.utils;

/**
 * Represents the different types of income a user can have.
 * Includes predefined options as well as a `CUSTOM` option for user-defined income sources.
 */
public enum IncomeType {
    SALARY,
    BONUS,
    INVESTMENT,
    RENTAL,
    BUSINESS,
    DIVIDEND,
    GIFT,
    CUSTOM
}
